class Calculator:
    def __init__(self,a,b) -> None:
        self.a = a
        self.b = b
    def add(self):
        self.c = self.a + self.b
        return self.c
    def multiply(Self):
        Self.c = Self.a*Self.b
        return Self.c
    def subtract(self):
        self.c = self.a - self.b
        return self.c
    def quotient(self):
        self.c = self.a / self.b
        return self.c
    def remainder(self):
        self.c = self.a%self.b
        return self.c
    
